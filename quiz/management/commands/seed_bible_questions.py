class Command(BaseCommand):
    help = 'Seeds the database with Arabic Bible quiz questions.'

    def handle(self, *args, **options):
        created = 0
        existing = 0
        for text, opts, correct, ref in QUESTIONS:
            q, was_created = BibleQuestion.objects.get_or_create(
                text=text,
                defaults={
                    'option_a': opts[0],
                    'option_b': opts[1],
                    'option_c': opts[2],
                    'option_d': opts[3],
                    'correct': correct,
                    'reference': ref,
                    'is_active': True,
                },
            )
            if was_created:
                created += 1
            else:
                existing += 1
        self.stdout.write(self.style.SUCCESS(
            f'Bible questions: created={created}, already-present={existing}, total={BibleQuestion.objects.count()}'
        ))
