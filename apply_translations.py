# ─── APPLY TRANSLATIONS TO DJANGO.PO ─────────────────────────────────────────

PO_PATH = Path('locale/ar/LC_MESSAGES/django.po')

def fix_header(content):
    """Fix the .po header so compilemessages doesn't barf."""
    content = content.replace(
        '"Content-Type: text/plain; charset=CHARSET\\n"',
        '"Content-Type: text/plain; charset=UTF-8\\n"',
    )
    content = content.replace(
        '"Language-Team: LANGUAGE <LL@li.org>\\n"',
        '"Language-Team: Arabic\\n"',
    )
    content = content.replace(
        '"Plural-Forms: nplurals=INTEGER; plural=EXPRESSION;\\n"',
        '"Plural-Forms: nplurals=6; plural=(n==0 ? 0 : n==1 ? 1 : n==2 ? 2 : n%100>=3 && n%100<=10 ? 3 : n%100>=11 ? 4 : 5);\\n"',
    )
    return content


def apply_translations(content):
    """Replace empty msgstr "" pairs with actual translations."""
    # Pattern: msgid "..." \n msgstr ""
    # We replace msgstr "" with msgstr "TRANSLATION" when we have a match
    pattern = re.compile(
        r'(msgid\s+"((?:[^"\\]|\\.)*)"\s*\n'
        r'msgstr\s+)""',
        re.MULTILINE
    )
    matched = 0
    missing = []

    def repl(m):
        nonlocal matched
        full_prefix = m.group(1)
        msgid = m.group(2)
        # Unescape backslash sequences
        unescaped = msgid.replace('\\"', '"').replace('\\n', '\n')
        if unescaped in T:
            translation = T[unescaped]
            escaped = translation.replace('\\', '\\\\').replace('"', '\\"').replace('\n', '\\n')
            matched += 1
            return f'{full_prefix}"{escaped}"'
        else:
            if unescaped:  # skip empty msgid (header)
                missing.append(unescaped)
            return m.group(0)

    new_content = pattern.sub(repl, content)
    return new_content, matched, missing


if __name__ == '__main__':
    content = PO_PATH.read_text(encoding='utf-8')
    content = fix_header(content)
    content, matched, missing = apply_translations(content)
    PO_PATH.write_text(content, encoding='utf-8')
    print(f"✓ Translated {matched} strings")
    print(f"⚠ Missing translations: {len(missing)}")
    if missing:
        print("\nFirst 20 missing strings:")
        for s in missing[:20]:
            preview = s if len(s) < 80 else s[:80] + '...'
            print(f"  - {preview!r}")
